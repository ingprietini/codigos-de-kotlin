fun main() {
    val entero: Int = 10
    val flotante: Float = 3.14f
    val doble: Double = 2.718281828

    val suma = entero + 5
    val resta = flotante - 1.14f
    val multiplicacion = entero * 2
    val division = doble / 2

    println("Suma: $suma")
    println("Resta: $resta")
    println("Multiplicación: $multiplicacion")
    println("División: $division")
}
